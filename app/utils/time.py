"""
Модуль для роботи з датою та часом.

Цей модуль містить утилітарні функції для роботи з датою та часом,
з урахуванням налаштованого часового поясу.
"""

from datetime import datetime

from app.const import TIMEZONE


def datetime_now() -> datetime:
    """
    Повертає поточну дату та час з налаштованим часовим поясом.
    
    Ця функція створює об'єкт datetime з поточним часом та встановленим
    часовим поясом з констант додатку.
    
    Returns:
        datetime: Об'єкт datetime з поточною датою та часом у вказаному часовому поясі
    """
    return datetime.now(tz=TIMEZONE)
